# 7. Реалізуйте генератор, який приймає на вхід будь-яку ітерабельну
# послідовність (рядок, список, кортеж) і повертає генератор, який
# буде вертати значення з цієї послідовності, при цьому, якщо було
# повернено останній елемент із послідовності - ітерація починається знову.

def generator(sequence):
    while True:
        for i in sequence:
            yield(i)

for elem in generator([1, 2, 3]):
    print(elem)
